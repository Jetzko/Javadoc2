/**
 * This class set methods to do arithmetic operations, by Andrea
 */
public class Operators {
    /**
     * This method takes second variable and adds to the first one
     * @param x first variable
     * @param y second variable
     * @return Operation's result
     */
    public int sum(int x, int y){
    return x + y;
}

    /**
     * This method takes second variable and subtracts to the first one
     * @param x first variable
     * @param y second variable
     * @return Operation's result
     */
    public int sub(int x, int y){
    return x - y;
}

    /**
     * This method takes second variable and multiplies to the first one
     * @param x first variable
     * @param y second variable
     * @return Operation's result
     */
    public int mol(int x, int y){
    return x * y;
}

    /**
     * This method takes first variable and divides for the second one
     * @param x first variable
     * @param y second variable
     * @return Operation's result
     */
    public int div(int x, int y){
    return x / y;
}
}
